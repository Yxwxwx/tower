# ═══════════════════════════════════════════════════════════════════
# Pre-computation: Slurm (shared)
# ═══════════════════════════════════════════════════════════════════

def generate_slurm(state) -> dict:
    """Generate rough Slurm template for HPC refinement."""
    task = state.task
    params = task.params

    slurm = f"""#!/bin/bash
#SBATCH --job-name={task.task_id}
#SBATCH --mem={getattr(params, 'max_memory_mb', 8000)}
#SBATCH --cpus-per-task={getattr(params, 'nprocs', 8)}
#SBATCH --time=24:00:00
# ROUGH TEMPLATE — HPC agent refines partition/account/node constraints

python {state.scratchpad.get('py_path', f'jobs/{task.task_id}/run.py')} > jobs/{task.task_id}/pyscf.log 2>&1
"""

    return {
        "node_history": state.node_history + ["generate_slurm"],
        **_update_scratchpad(state,
            slurm_content=slurm,
            slurm_path=f"jobs/{task.task_id}/pyscf_rough.sh",
        ),
    }


