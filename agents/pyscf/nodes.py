def pre_done(state) -> dict:
    """Return generated artifacts to supervisor."""
    task_id = state.task_id

    return {
        "status": TaskStatus.DONE,
        "artifacts_out": [
            Artifact(artifact_id=f"{task_id}-script",
                     path=state.scratchpad["py_path"],
                     type="gjf",  # PySCF script = "gjf" for generic input
                     description="PySCF Python script",
                     producer_agent="pyscf", producer_task_id=task_id),
            Artifact(artifact_id=f"{task_id}-slurm",
                     path=state.scratchpad["slurm_path"],
                     type="slurm",
                     description="Rough Slurm template for HPC refinement",
                     producer_agent="pyscf", producer_task_id=task_id),
        ],
        "node_history": state.node_history + ["pre_done"],
    }


# ═══════════════════════════════════════════════════════════════════
# Post-computation: parse + register (shared)
# ═══════════════════════════════════════════════════════════════════

def read_output(state) -> dict:
    """Read PySCF output log from HPC execution."""
    log_id = ""
    for ref in state.task.artifacts_in:
        if ref.type in ("log",):
            log_id = ref.artifact_id

    return {
        "node_history": state.node_history + ["read_output"],
        **_update_scratchpad(state, log_artifact_id=log_id),
    }


def parse_energy(state) -> dict:
    """Parse SCF or CASSCF energy from PySCF log.

    Extracts:
    - E(RHF) / E(CASSCF) from output
    - Convergence status
    - Wall time

    TODO: computational scientist implements actual regex parsing.
    """
    params = state.task.params

    if params.job_type == "CASSCF":
        result = PySCFResult(casscf_energy=None, converged=True)
    else:
        result = PySCFResult(scf_energy=None, converged=True)

    return {
        "result_data": result,
        "node_history": state.node_history + ["parse_energy"],
    }


def register_artifacts(state) -> dict:
    """Surface HPC-produced artifacts for downstream or supervisor."""
    log_id = state.scratchpad.get("log_artifact_id", "")
    artifacts = []
    if log_id:
        artifacts.append(Artifact(
            artifact_id=log_id, path="", type="log",
            description="PySCF output log (HPC-produced)",
            producer_agent="pyscf", producer_task_id=state.task_id,
            status=ArtifactStatus.READY,
        ))

    return {
        "status": TaskStatus.DONE,
        "artifacts_out": artifacts,
        "node_history": state.node_history + ["register_artifacts"],
    }
