"""PySCF agent nodes — RHF/DFT and CASSCF paths.

Pre-computation:
- rhf_dft: generate RHF/DFT Python script + rough slurm
- casscf: read fchk → select orbitals → generate CASSCF script + slurm

Post-computation:
- parse_output: parse SCF/CASSCF energy from PySCF log
- register_artifacts: surface results for downstream agents
"""
from contracts.pyscf_task import PySCFResult
from contracts.agent_task import Artifact, ArtifactStatus, TaskStatus


def _update_scratchpad(state, **kwargs) -> dict:
    return {"scratchpad": {**state.scratchpad, **kwargs}}


# ═══════════════════════════════════════════════════════════════════
# Pre-computation: RHF / DFT
# ═══════════════════════════════════════════════════════════════════

def generate_rhf_input(state) -> dict:
    """Generate PySCF RHF/DFT Python script.

    Produces a self-contained .py file that:
    - Builds molecule from geometry
    - Sets up SCF with specified basis/functional
    - Runs kernel() and prints energy

    TODO: computational scientist fills in actual geometry + template.
    """
    task = state.task
    params = task.params

    # Build a general PySCF RHF script template
    script = f'''"""PySCF {params.job_type} calculation — generated by Tower."""
from pyscf import gto, scf
'''
    if params.job_type in ("RDFT", "UDFT") and params.functional:
        script += f"from pyscf import dft\n"

    script += f'''
# --- Molecule definition ---
mol = gto.Mole()
mol.atom = ""\"# TODO: computational scientist provides geometry
# Example: N 0 0 0; N 0 0 1.1
""\"
mol.basis = "{params.basis}"
mol.charge = {params.charge}
mol.spin = {params.spin}
mol.build()

# --- SCF calculation ---
'''
    if params.job_type in ("RDFT", "UDFT"):
        script += f'''mf = dft.RKS(mol) if "{params.job_type}".startswith("R") else dft.UKS(mol)
mf.xc = "{params.functional or 'b3lyp'}"
'''
    elif params.job_type.startswith("U"):
        script += "mf = scf.UHF(mol)\n"
    else:
        script += "mf = scf.RHF(mol)\n"

    script += f'''
mf.kernel()

# --- Output ---
print(f"SCF converged: {{mf.converged}}")
print(f"E({{mf.__class__.__name__}}) = {{mf.e_tot:.8f}} Ha")
'''

    return {
        "node_history": state.node_history + ["generate_rhf_input"],
        **_update_scratchpad(state,
            py_script=script,
            py_path=f"jobs/{task.task_id}/{task.task_id}.py",
        ),
    }


